package main

import "fmt"

func main() {

	a := 10
	b := 5

	// Lebih Dari
	fmt.Println(a > b)
	//  Kurang Dari
	fmt.Println(a < b)
	//  Lebih Dari Sama dengan
	fmt.Println(a >= b)
	//  Kurang Dari Sama Dengan
	fmt.Println(a <= b)
	//  Sama dengan
	fmt.Println(a == b)
	//  Tidak Sama Dengan
	fmt.Println(a != b)
}
